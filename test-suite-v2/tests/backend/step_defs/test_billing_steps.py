@then(parsers.parse("the usage report should show at least {n:d} total_requests_30d"))
def usage_at_least(client, context, n):
    key_id = context["api_key_id"]
    r = client.get(f"/auth/keys/{key_id}/usage",
        headers=context["customer_headers"],
    )
    assert r.status_code == 200
    assert r.json().get("total_requests_30d", 0) >= n, \
        f"Usage: {r.json().get('total_requests_30d')}, expected >= {n}"


@then("the daily breakdown should contain today's date")
def daily_has_today(client, context):
    from datetime import date
    key_id = context["api_key_id"]
    r = client.get(f"/auth/keys/{key_id}/usage",
        headers=context["customer_headers"],
    )
    today   = date.today().isoformat()
    entries = [d["usage_date"] for d in r.json().get("daily", [])]
    assert today in entries, f"Today {today} not in daily entries: {entries}"


